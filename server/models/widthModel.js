const { default: mongoose } = require("mongoose");


const widthSchema = mongoose.Schema({
    name: {
        type: Number,
        required: true
    },
    createdAt: {
        type: Date,
        default: Date.now
    },
    updatedAt: {
        type: Date,
        default: Date.now
    }
});

module.exports = mongoose.model('Width', widthSchema);